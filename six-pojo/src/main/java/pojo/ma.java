package pojo;

public class ma {
    private Integer haoma;
    private Integer monny;

    public Integer getHaoma() {
        return haoma;
    }

    public void setHaoma(Integer haoma) {
        this.haoma = haoma;
    }

    public Integer getMonny() {
        return monny;
    }

    public void setMonny(Integer monny) {
        this.monny = monny;
    }

    @Override
    public String toString() {
        return "ma{" +
                "haoma=" + haoma +
                ", monny=" + monny +
                '}';
    }
}
